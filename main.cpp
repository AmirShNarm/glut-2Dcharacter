void drawConcaveShape(float r,float g,float b,GLfloat pointArray[][3],int arrayLenth){
    R_color=r;
    G_color=g;
    B_color=b;
GLUtesselator *tobj = gluNewTess();

gluTessCallback(tobj, GLU_TESS_VERTEX,
                   (GLvoid (*) ()) &glVertex3dv);
gluTessCallback(tobj, GLU_TESS_BEGIN,
                   (GLvoid (*) ()) &beginCallback);
gluTessCallback(tobj, GLU_TESS_END,
                   (GLvoid (*) ()) &endCallback);
gluTessCallback(tobj, GLU_TESS_ERROR,
                   (GLvoid (*) ()) &errorCallback);
gluTessCallback(tobj, GLU_TESS_COMBINE,
                   (GLvoid (*) ()) &combineCallback);
gluTessCallback(tobj, GLU_TESS_VERTEX,
                   (GLvoid (*) ()) &vertexCallback);
gluTessProperty(tobj, GLU_TESS_WINDING_RULE,
                   GLU_TESS_WINDING_POSITIVE);
gluTessBeginPolygon(tobj, NULL);
      gluTessBeginContour(tobj);

      for(int i=0;i<arrayLenth;i++){
         gluTessVertex(tobj,(GLdouble*)pointArray[i],pointArray[i]);
         }
      gluTessEndContour(tobj);
   gluTessEndPolygon(tobj);

gluDeleteTess(tobj);


};


void drawCircle(float r,float g,float b,int radius,float R_radius,int X_center,int Y_center,int AmountFans,GLenum mode){

    GLfloat twicePi = 2.0f * 3.14;
    glBegin(mode);
        glColor3f(r,g,b);
        glVertex2f(X_center, Y_center); // center of circle
        if(R_radius>1){
        for(int i = 0; i <= AmountFans;i++) {
            glVertex2f(
                    X_center + (radius * cos(i *  twicePi / AmountFans)),
                Y_center + (radius * (1 - R_radius) * sin(i * twicePi / AmountFans))
            );
        }
        }
        if(R_radius<1){
        for(int i = 0; i <= AmountFans;i++) {
            glVertex2f(
                    X_center + (radius *(1 - R_radius)* cos(i *  twicePi / AmountFans)),
                Y_center + (radius * sin(i * twicePi / AmountFans))
            );
        }
        }
        if(R_radius==1){
        for(int i = 0; i <= AmountFans;i++) {
            glVertex2f(
                    X_center + (radius * cos(i *  twicePi / AmountFans)),
                Y_center + (radius * sin(i * twicePi / AmountFans))
            );
        }
        }
        glColor3f(0.0,0.0,0.0);
    glEnd();

}

